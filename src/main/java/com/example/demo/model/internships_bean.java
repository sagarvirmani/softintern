package com.example.demo.model;

public class internships_bean {

		private int intern_id;
		private String company_name;
		private String title;
		private String description;
		private String stipend;
		private String duration;
		private String required_skills;
		private String eligibility;
		private String number_of_vacancies;
		private String city;
		private String state;
		
		public String getCity() {
			return city;
		}
		public void setCity(String city) {
			this.city = city;
		}
		public String getState() {
			return state;
		}
		public void setState(String state) {
			this.state = state;
		}
		public int getIntern_id() {
			return intern_id;
		}
		public void setIntern_id(int intern_id) {
			this.intern_id = intern_id;
		}
		public String getCompany_name() {
			return company_name;
		}
		public void setCompany_name(String company_name) {
			this.company_name = company_name;
		}
		public String getTitle() {
			return title;
		}
		public void setTitle(String title) {
			this.title = title;
		}
		public String getDescription() {
			return description;
		}
		public void setDescription(String description) {
			this.description = description;
		}
		public String getStipend() {
			return stipend;
		}
		public void setStipend(String stipend) {
			this.stipend = stipend;
		}
		public String getDuration() {
			return duration;
		}
		public void setDuration(String duration) {
			this.duration = duration;
		}
		public String getRequired_skills() {
			return required_skills;
		}
		public void setRequired_skills(String required_skills) {
			this.required_skills = required_skills;
		}
		public String getEligibility() {
			return eligibility;
		}
		public void setEligibility(String eligibility) {
			this.eligibility = eligibility;
		}
		public String getNumber_of_vacancies() {
			return number_of_vacancies;
		}
		public void setNumber_of_vacancies(String number_of_vacancies) {
			this.number_of_vacancies = number_of_vacancies;
		}
		
		
}
